import tensorflow as tf
import numpy      as np

from qfast import Block


class TestBlockConstructor ( tf.test.TestCase ):

    TOFFOLI = np.asarray(
            [[1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j]] )

    INVALID = np.asarray(
            [[1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j]] )

    def test_block_constructor_invalid_type ( self ):
        # Unitary Type
        self.assertRaises( TypeError, Block, 1, (0, 1) )
        self.assertRaises( TypeError, Block, np.array( [ 0, 1 ] ), (0, 1) )
        self.assertRaises( TypeError, Block, np.array( [ [ [ 0 ] ] ] ), (0, 1) )

        # Location Type
        self.assertRaises( TypeError, Block, self.TOFFOLI, 1 )
        self.assertRaises( TypeError, Block, self.TOFFOLI, ("a", "b") )
        self.assertRaises( TypeError, Block, self.TOFFOLI, (1, 1) )

    def test_block_constructor_invalid_value ( self ):
        self.assertRaises( ValueError, Block, self.TOFFOLI, (0, 1) )
        self.assertRaises( ValueError, Block, self.TOFFOLI, (0, 1, 2, 3) )
        self.assertRaises( ValueError, Block, self.INVALID, (0, 1, 2) )

        invalid_utry_matrix = np.copy( self.TOFFOLI )
        invalid_utry_matrix[4][4] = 137.+0.j

        self.assertRaises( ValueError, Block, invalid_utry_matrix, (0, 1, 2) )

    def test_block_constructor_valid ( self ):
        block = Block( self.TOFFOLI, (0, 1, 2) )
        self.assertTrue( np.array_equal( block.utry, self.TOFFOLI ) )
        self.assertTrue( np.array_equal( block.loc, (0, 1, 2) ) )
        self.assertEqual( block.num_qubits, 3 )
