import tensorflow as tf
import numpy      as np

from qfast import Circuit


class TestCircuitConstructor ( tf.test.TestCase ):

    TOFFOLI = np.asarray(
            [[1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j]] )

    INVALID = np.asarray(
            [[1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j],
            [0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 0.+0.j, 1.+0.j, 0.+0.j]] )

    def test_circuit_constructor_invalid_type ( self ):
        self.assertRaises( TypeError, Circuit, 1 )
        self.assertRaises( TypeError, Circuit, np.array( [ 0, 1 ] ) )
        self.assertRaises( TypeError, Circuit, np.array( [ [ [ 0 ] ] ] ) )
        self.assertRaises( TypeError, Circuit, self.INVALID )

    def test_circuit_constructor_invalid_value ( self ):
        invalid_utry_matrix = np.copy( self.TOFFOLI )
        invalid_utry_matrix[4][4] = 137.+0.j

        self.assertRaises( ValueError, Circuit, invalid_utry_matrix )

    def test_circuit_constructor_valid ( self ):
        circ = Circuit( self.TOFFOLI )
        self.assertTrue( np.array_equal( circ.utry, self.TOFFOLI ) )
        self.assertEqual( circ.num_qubits, 3 )
        self.assertEqual( len( circ.blocks ), 1 )
        self.assertTrue( np.array_equal( circ.blocks[0].utry, self.TOFFOLI ) )
        self.assertTrue( np.array_equal( circ.blocks[0].loc, (0, 1, 2) ) )
        self.assertEqual( circ.blocks[0].num_qubits, 3 )
